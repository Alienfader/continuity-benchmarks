#!/usr/bin/env python3
"""
Generate a benchmark quiz from a real Continuity workspace's decisions.

Reads `<workspace>/.continuity/decisions.json` and `code-links.json`,
filters to decisions that meet quality criteria for a recall-style quiz,
and emits a quiz JSON file that `runners/middleware-replay.ts` can
consume via its `--quiz <path>` flag.

Quality filter:
  1. Decision is linked in code-links.json (so auto-middleware can fire)
  2. Decision's question starts with "Why" or "How" (architectural rationale shape)
  3. Decision's answer is at least 200 chars (substantive, not "(commit abc123)")
  4. Decision is not superseded
  5. Decision references a path that's NOT package.json (too generic)

Selection: deterministic (sorted by id, take first N matching).

Usage:
  python3 scripts/generate-workspace-quiz.py \\
    --workspace /path/to/continuity-ultimate \\
    --output prompts/quizzes/continuity-ultimate.json \\
    --count 20
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path
from typing import Any


def load_json(p: Path) -> Any:
    return json.loads(p.read_text())


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--workspace", required=True, help="Path to a Continuity workspace root")
    ap.add_argument("--output", required=True, help="Output quiz JSON path")
    ap.add_argument("--count", type=int, default=20, help="Number of questions to pick")
    ap.add_argument(
        "--min-answer-chars", type=int, default=200, help="Min chars in answer for inclusion"
    )
    ns = ap.parse_args()

    ws = Path(ns.workspace).resolve()
    decisions_path = ws / ".continuity" / "decisions.json"
    links_path = ws / ".continuity" / "code-links.json"

    if not decisions_path.exists():
        print(f"missing {decisions_path}", file=sys.stderr)
        sys.exit(1)
    if not links_path.exists():
        print(
            f"WARNING: missing {links_path} — auto-middleware will no-op",
            file=sys.stderr,
        )

    decisions_raw = load_json(decisions_path)
    decisions = decisions_raw if isinstance(decisions_raw, list) else decisions_raw.get(
        "decisions", []
    )

    links_raw = load_json(links_path) if links_path.exists() else {"links": []}
    links = links_raw.get("links", links_raw) if isinstance(links_raw, dict) else links_raw

    # Build set of decision IDs that are linked to NON-trivial files.
    trivial_paths = {"package.json", "package-lock.json", "tsconfig.json"}
    linked_ids: set[str] = set()
    for link in links:
        fp = link.get("location", {}).get("filePath", "")
        if not fp:
            continue
        # Reject too-generic paths
        from os.path import basename

        if basename(fp) in trivial_paths:
            continue
        for did in link.get("linkedDecisionIds", []):
            linked_ids.add(did)

    # Filter decisions.
    qualified: list[dict[str, Any]] = []
    for d in decisions:
        if not isinstance(d, dict):
            continue
        did = d.get("id")
        if not did or did not in linked_ids:
            continue
        question = d.get("question", "")
        if not isinstance(question, str):
            continue
        q_lower = question.strip().lower()
        if not (q_lower.startswith("why") or q_lower.startswith("how")):
            continue
        answer = d.get("answer", "")
        if not isinstance(answer, str) or len(answer) < ns.min_answer_chars:
            continue
        status = d.get("status", "active")
        if status in ("superseded", "deprecated", "outdated"):
            continue
        qualified.append(d)

    if len(qualified) < ns.count:
        print(
            f"WARNING: only {len(qualified)} qualified decisions; requested {ns.count}",
            file=sys.stderr,
        )

    # Deterministic order: sort by id.
    qualified.sort(key=lambda d: d["id"])
    picked = qualified[: ns.count]

    quiz = {
        "project": ws.name,
        "description": (
            f"Auto-generated quiz from {ws.name}'s production decision corpus. "
            f"{len(picked)} questions, each from a code-linked architectural-rationale decision. "
            "Generated by scripts/generate-workspace-quiz.py."
        ),
        "source_workspace": str(ws),
        "questions": [
            {
                "id": f"Q{i+1}",
                "decisionId": d["id"],
                "question": d["question"].strip(),
                "groundTruth": d["answer"].strip(),
                "type": "direct-recall",
                "correctAnswerAt": None,
            }
            for i, d in enumerate(picked)
        ],
    }

    out = Path(ns.output).resolve()
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(json.dumps(quiz, indent=2) + "\n")
    print(f"wrote {len(picked)} questions to {out}")
    print(f"  candidates considered: {len(qualified)}/{len(decisions)} decisions")
    print(f"  workspace: {ws}")


if __name__ == "__main__":
    main()
